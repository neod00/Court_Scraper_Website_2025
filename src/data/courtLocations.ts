// 법원명에서 좌표 찾기 (부분 매칭 지원)
export function findCourtLocation(courtName: string): CourtLocation | null {
    // 정확한 매칭 시도
    if (courtLocations[courtName]) {
        return courtLocations[courtName];
    }

    // 부분 매칭 시도
    for (const [name, location] of Object.entries(courtLocations)) {
        if (courtName.includes(name) || name.includes(courtName)) {
            return location;
        }
    }

    return null;
}
